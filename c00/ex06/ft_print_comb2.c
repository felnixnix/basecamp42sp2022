#include <unistd.h>

void ft_print_comb2(void)
{
	char	a[5];
	char	b;

	a[2] = ' ';
	a[0] = '0' - 1;
	while (a[0]++ < '9')
	{
		a[1] = '0' - 1;
		while (a[1]++ < '9')
		{
			a[3] = a[0] - 1;
			b = a[1];
			while (a[3]++ < '9')
			{
				a[4] = b;
				b = '0' - 1;
				while(a[4]++ < '9')
				{
					write (1, a, 5);
						if (a[0] != '9' || a[1] != '8')
						write (1, ", ", 2);
				}
			}
		}
	}
}
